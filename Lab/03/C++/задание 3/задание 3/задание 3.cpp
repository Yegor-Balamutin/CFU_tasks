

#include <iostream>

using namespace std;

int main()
{
    double a;
    int b;
    cout << "a= ";
    cin >> a;
    cout << "b= ";
    cin >> b;
    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << endl;
}

